void _run_spikegeneratorgroup_codeobject()
{	
	using namespace brian;

    const double _start_time = omp_get_wtime();

	///// CONSTANTS ///////////
	int32_t* const _array_spikegeneratorgroup_spike_number = _dynamic_array_spikegeneratorgroup_spike_number.empty()? 0 : &_dynamic_array_spikegeneratorgroup_spike_number[0];
const int _numspike_number = _dynamic_array_spikegeneratorgroup_spike_number.size();
const int _num_spikespace = 101;
const int _num_lastindex = 1;
double* const _array_spikegeneratorgroup_spike_time = _dynamic_array_spikegeneratorgroup_spike_time.empty()? 0 : &_dynamic_array_spikegeneratorgroup_spike_time[0];
const int _numspike_time = _dynamic_array_spikegeneratorgroup_spike_time.size();
const int _numperiod = 1;
int32_t* const _array_spikegeneratorgroup_neuron_index = _dynamic_array_spikegeneratorgroup_neuron_index.empty()? 0 : &_dynamic_array_spikegeneratorgroup_neuron_index[0];
const int _numneuron_index = _dynamic_array_spikegeneratorgroup_neuron_index.size();
const int _numt = 1;
const int _numdt = 1;
	///// POINTERS ////////////
 	
 int32_t* __restrict  _ptr_array_spikegeneratorgroup_spike_number = _array_spikegeneratorgroup_spike_number;
 int32_t* __restrict  _ptr_array_spikegeneratorgroup__spikespace = _array_spikegeneratorgroup__spikespace;
 int32_t* __restrict  _ptr_array_spikegeneratorgroup__lastindex = _array_spikegeneratorgroup__lastindex;
 double* __restrict  _ptr_array_spikegeneratorgroup_spike_time = _array_spikegeneratorgroup_spike_time;
 double* __restrict  _ptr_array_spikegeneratorgroup_period = _array_spikegeneratorgroup_period;
 int32_t* __restrict  _ptr_array_spikegeneratorgroup_neuron_index = _array_spikegeneratorgroup_neuron_index;
 double* __restrict  _ptr_array_defaultclock_t = _array_defaultclock_t;
 double* __restrict  _ptr_array_defaultclock_dt = _array_defaultclock_dt;



    const double _the_period = _ptr_array_spikegeneratorgroup_period[0];
    const double padding_before = fmod(_ptr_array_defaultclock_t[0], _the_period);
    const double padding_after  = fmod(_ptr_array_defaultclock_t[0]+_ptr_array_defaultclock_dt[0], _the_period);
    const double epsilon        = 1e-3*_ptr_array_defaultclock_dt[0];

    // We need some precomputed values that will be used during looping
    const bool not_end_period  = (fabs(padding_after) > epsilon) && (fabs(padding_after) < (_the_period - epsilon));
    bool test;

    // TODO: We don't deal with more than one spike per neuron yet
    long _cpp_numspikes = 0;

    for(int _idx=_ptr_array_spikegeneratorgroup__lastindex[0]; _idx < _numspike_time; _idx++)
    {
        if (not_end_period)
            test = (_ptr_array_spikegeneratorgroup_spike_time[_idx] > padding_after) || (fabs(_ptr_array_spikegeneratorgroup_spike_time[_idx] - padding_after) < epsilon);
        else
            // If we are in the last timestep before the end of the period, we remove the first part of the
            // test, because padding will be 0
            test = (fabs(_ptr_array_spikegeneratorgroup_spike_time[_idx] - padding_after) < epsilon);
        if (test)
            break;
        _ptr_array_spikegeneratorgroup__spikespace[_cpp_numspikes++] = _ptr_array_spikegeneratorgroup_neuron_index[_idx];
    }

    _ptr_array_spikegeneratorgroup__spikespace[100] = _cpp_numspikes;

    // If there is a periodicity in the SpikeGenerator, we need to reset the lastindex
    // when all spikes have been played and at the end of the period
    if (! not_end_period)
        _ptr_array_spikegeneratorgroup__lastindex[0] = 0;
    else
        _ptr_array_spikegeneratorgroup__lastindex[0] += _cpp_numspikes;



    const double _run_time = omp_get_wtime() -_start_time;
    spikegeneratorgroup_codeobject_profiling_info += _run_time;
}


